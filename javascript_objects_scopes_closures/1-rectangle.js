#!/usr/bin/node
/**
 * Rectangle - A class that defines a rectangle.
 * @class
 */
class Rectangle {
    /**
     * Creates an instance of Rectangle.
     * @param {number} w - The width of the rectangle.
     * @param {number} h - The height of the rectangle.
     */
    constructor(w, h) {
      this.width = w;
      this.height = h;
    }
  }
  
  module.exports = Rectangle;
  